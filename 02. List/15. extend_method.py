# The extend() method in Python is used with lists to add multiple elements from another iterable (like a list, tuple, set, or string) to the end of the current list.
# It extends the list by appending all the elements from the passed iterable.

list1 = [1,3,5,6,7]
list2 = [2,6,7,8]

list1.extend(list2)

print(list1)


