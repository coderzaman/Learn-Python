# a. append() Method
# Adds a single element to the end of the list.
# Syntax
# my_list.append(element)
# Example:

fruits = ["apple", "banana", "cherry"]

fruits.append(10)
print(fruits)

# b. insert() Method
# Inserts an element at a specified index.
fruits = ["apple", "banana", "cherry"]
fruits.insert(1, "Orange")
print(fruits)

# c. extend() Method
fruits.extend([10,23,42,12,24])
print(fruits)
