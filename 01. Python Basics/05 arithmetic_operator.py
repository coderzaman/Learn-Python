# Addition (+)
a = 10
b = 5
result = a + b
print(result)  # Output: 15

# . Subtraction (-)
a = 10
b = 5
result = a - b
print(result)  # Output: 5

# Multiplication (*)
a = 10
b = 5
result = a * b
print(result)  # Output: 50

# Division( /) Divides the first number by the second. Always returns a float, even if the division is exact.
a = 10
b = 5
result = a / b
print(result)  # Output: 2.0

# Division resulting in a float
c = 7
d = 2
result = c / d
print(result)  # Output: 3.5

# Floor Division (//) Divides and returns the integer part of the quotient, effectively rounding down to the nearest whole number.
a = 10
b = 3
result = a // b
print(result)  # Output: 3

c = -7
d = 2
result = c // d
print(result)  # Output: -4

# Modulus (%)
a = 10
b = 3
result = a % b
print(result)  # Output: 1

c = 9
d = 2
result = c % d
print(result)  # Output: 1

# Exponentiation(**)
a = 2
b = 3
result = a ** b
print(result)  # Output: 8

c = 5
d = 0
result = c ** d
print(result)  # Output: 1

# Parentheses (())
# Used to control the order in which operations are performed, overriding the default operator precedence.
result = (5 + 3) * 2
print(result)  # Output: 16

# Without parentheses
result = 5 + 3 * 2
print(result)  # Output: 11





