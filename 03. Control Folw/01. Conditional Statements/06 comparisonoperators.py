x = 10
y = 20

# Equal to
print(x == y)  # Output: False

# Not equal to
print(x != y)  # Output: True

# Greater than
print(x > y)   # Output: False

# Less than
print(x < y)   # Output: True

# Greater than or equal to
print(x >= 10) # Output: True

# Less than or equal to
print(y <= 20) # Output: True
